package com.bridgelabz.DLS_Assinment_Program;

public class PrimitiveDataTypes {
    static byte x;
    static short y;
    static int a;
    static long b;
    static float c;
    static double d;
    static char e;
    static boolean f;



    public static void main(String[] args) {
        System.out.println("Default values of integer.....");
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("Default value of float");
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("Default value of character");
        System.out.println("e = " + e);
        System.out.println("Default value of boolean");
        System.out.println("f = " + f);
    }
}
