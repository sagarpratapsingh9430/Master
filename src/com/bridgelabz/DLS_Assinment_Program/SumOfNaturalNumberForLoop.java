package com.bridgelabz.DLS_Assinment_Program;

public class SumOfNaturalNumberForLoop {
    public static void main(String []args){
        int n=10;
        int sum=0;
        for (int i=0;i<=n;i++){
            sum=sum+i;
        }
        System.out.println("Sum of ten natural number is: " + sum);
    }
}
