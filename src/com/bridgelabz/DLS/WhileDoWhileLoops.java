package com.bridgelabz.DLS;

public class WhileDoWhileLoops {
    public static void main(String [] args){

         int m=0;
         while (m<10){
             System.out.println(m);
             m++;
         }

         int n=4;
         do {
             System.out.println(n);
             n++;
         }while (n<=15);

    }
}
